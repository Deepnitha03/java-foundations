public class VariablesDemo {
    public static void main(String[] args) {
        int age = 17;
        double height = 168.5;
        char grade = 'A';
        String name = "Deepnitha";

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
        System.out.println("Grade: " + grade);
    }
}
